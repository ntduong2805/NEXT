const useCountries = () => {
  // Format the countries data and store it in formattedCountries
  const formattedCountries = formatCountries(countries);

  const getAll = () => formattedCountries;

  const getByValue = (value) => {
    return formattedCountries.find((country) => country.value === value);
  };

  return {
    getAll,
    getByValue,
  };
};

